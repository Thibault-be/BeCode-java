public class AllPrimitiveDataTypes {

  public static void main(String[] args){

    // all types: byte, char, int, float, long, double, boolean, short

    byte  a = -126;
    char b = '+';
    int c = 5;
    float d = 5.0f;
    long e = 256987L;
    double f = 45.5468;
    boolean g = true;
    short h = 26487;

    System.out.println("byte a " + a);
    System.out.println("char b " + b);
    System.out.println("int c " + c);
    System.out.println("float d " + d);
    System.out.println("long e " + e);
    System.out.println("double f" + f);
    System.out.println("boolean g "  + g);
    System.out.println("short h " + h);
    
  }
}